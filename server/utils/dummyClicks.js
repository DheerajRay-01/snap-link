export const getClickAnalytics =  (clickRes) => {
  // Initialize aggregates
  const browserData = {};
  const osData = {};
  const date = {};
  const ipSet = new Set();
  const cityData = {};
  const countryData = {};

  // Aggregate data in one loop
  clickRes.forEach(({ browser, os, ip, createdAt, city, country }) => {
    browserData[browser] = (browserData[browser] || 0) + 1;
    osData[os] = (osData[os] || 0) + 1;
    cityData[city] = (cityData[city] || 0) + 1;
    countryData[country] = (countryData[country] || 0) + 1;
    ipSet.add(ip);

    const newDate = new Date(createdAt).toLocaleDateString();
    date[newDate] = (date[newDate] || 0) + 1;
  });

  // Convert to array of objects
  const browserStats = Object.entries(browserData).map(([label, value]) => ({ label, value }));
  const osStats = Object.entries(osData).map(([label, value]) => ({ label, value }));
  const cityStats = Object.entries(cityData).map(([label, value]) => ({ label, value }));
  const countryStats = Object.entries(countryData).map(([label, value]) => ({ label, value }));
  const dateStats = Object.entries(date).map(([label, value]) => ({ label, value }));
  const uniqueIps = [...ipSet];

  // Final analytics object
  const analytics = {
    totalClicks: clickRes.length,            // ğŸ“Š Total Clicks
    totalUniqueUsers: uniqueIps.length,      // ğŸ‘¥ Total Unique Users
      browsers: browserStats,                   // ğŸ–¥ï¸ Browser Stats
    osData: osStats,                          // ğŸ’» OS Stats
    cityData: cityStats,                      // ğŸ™ï¸ City Stats
    countryData: countryStats,                // ğŸŒ Country Stats
    dateData: dateStats                       // ğŸ“… Clicks by Date
  };

  // ğŸ“ Optional: Log it out for debugging
  // console.log("ğŸ“Š Total Clicks:", analytics.totalClicks);
  // console.log("ğŸŒ Unique IPs:", analytics.uniqueIps);
  // console.log("ğŸ‘¥ Total Unique Users:", analytics.totalUniqueUsers);
  // console.log("ğŸ–¥ï¸ Browsers:", analytics.browsers);
  // console.log("ğŸ’» OS Data:", analytics.osData);
  // console.log("ğŸ™ï¸ City Data:", analytics.cityData);
  // console.log("ğŸŒ Country Data:", analytics.countryData);
  // console.log("ğŸ“… Date Data:", analytics.dateData);

  return analytics;
};


